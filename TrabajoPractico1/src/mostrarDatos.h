#ifndef MOSTRARDATOS_H_
#define MOSTRARDATOS_H_

/** \brief Muestra el resultado de la operacion suma
 *
 * \param resultadoObtenido int Recibe el resultado
 * \return void No retorna nada
 *
 */
void mostrarSuma(int resultadoObtenido);




/** \brief Muestra el resultado de la operacion resta
 *
 * \param resultadoObtenido int Recibe el resultado
 * \return void No retorna nada
 *
 */
void mostrarResta(int resultadoObtenido);



/** \brief Muestra el resultado de la operacion division
 *
 * \param caracterRecibido char Recibe el resultado como caracter
 * \return void No retorna nada
 *
 */
void mostrarDivision(float resultadoObtenido);




/** \brief Muestra el resultado de la operacion multiplicacion
 *
 * \param resultadoObtenido int Recibe el resultado
 * \return void No retorna nada
 *
 */
void mostrarMultiplicacion(int resultadoObtenido);





/** \brief Muestra el resultado de la operacion factorial
 *
 * \param resultadoPrimerOperando int Recibe el resultado del primer operando
 * \param resultadoSegundoOperando int Recibe el resultado del segundo operando
 * \return void No retorna nada
 *
 */
void mostrarFactorial(long int resultadoObtenido,char nombreNumero);



#endif /* MOSTRARDATOS_H_ */
